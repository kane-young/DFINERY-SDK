enum HTTPMethod: String {
  case get = "GET"
  case put = "PUT"
  case post = "POST"
  case delete = "DELETE"
  case head = "HEAD"
  case connect = "CONNECT"
  case options = "OPTIONS"
  case patch = "PATCH"
}
